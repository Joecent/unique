<script type="text/javascript">
import "@/utils/jquery-2.1.1.min.js";
import "@/assets/css/index1.css";
import "@/assets/css/common/common.css";
import "@/assets/css/common/css3.css";
import "@/assets/css/common/Initialization.css";
import "@/assets/css/Product.css";
import "@/assets/js/nav.js";
export default {
  data() {
    return {};
  },
  mounted() {
    (function() {
      var $imgs = $("#sortable li");
      var $buttons = $("#buttons");
      var tagged = {};

      $imgs.each(function() {
        var li = this;
        var tags = $(this).data("tags");

        if (tags) {
          tags.split(",").forEach(function(tagName) {
            if (tagged[tagName] == null) {
              tagged[tagName] = [];
            }
            tagged[tagName].push(li);
          });
        }
      });

      $("<li/>", {
        text: "全部",
        class: "active",
        click: function() {
          $(this)
            .addClass("active")
            .siblings()
            .removeClass("active");
          $imgs.hide().slideDown(500);
        }
      }).appendTo($buttons);

      $.each(tagged, function(tagName) {
        $("<li/>", {
          text: tagName,
          click: function() {
            $(this)
              .addClass("active")
              .siblings()
              .removeClass("active");
            $imgs
              .hide()
              .filter(tagged[tagName])
              .fadeIn(500);
          }
        }).appendTo($buttons);
      });
    })();
  }
};
</script>

